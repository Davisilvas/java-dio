package school;
public class Alumn {
    String name;
    int age;
    char sex;
    
    public String getName(){
        return name;
    }

    public void setName (String nameInput){
        this.name = nameInput;
    }

    public int getAge(){
        return age;
    }

    public void setAge (int ageInput){
        this.age = ageInput;
    }

    public char getSex() {
        return sex;
    }

    public void setSex(char sex) {
        this.sex = sex;
    }
}
